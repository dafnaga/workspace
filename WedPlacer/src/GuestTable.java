
public class GuestTable {
	private Boolean closeToMusic;
	private Integer tableNumber;
	
	public GuestTable(Integer tableNumber, Boolean closeToMusic){
		this.tableNumber = tableNumber;
		this.closeToMusic = closeToMusic;
	}
	
	public Boolean getCloseToMusic() {
		return closeToMusic;
	}

	public void setCloseToMusic(Boolean closeToMusic) {
		this.closeToMusic = closeToMusic;
	}

	public Integer getTableNumber() {
		return tableNumber;
	}

	public void setTableNumber(Integer tableNumber) {
		this.tableNumber = tableNumber;
	}
}
